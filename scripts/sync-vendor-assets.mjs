/**
 * Copy self-hosted fonts and Font Awesome into public/ for deploy.
 * Generates public/css/site-fonts.css (latin + latin-ext for EN / Albanian).
 */
import { cpSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

/** @type {{ pkg: string; css: string; family: string }[]} */
const FONT_CSS = [
  { pkg: "@fontsource/open-sans", css: "latin-400.css", family: "open-sans" },
  { pkg: "@fontsource/open-sans", css: "latin-500.css", family: "open-sans" },
  { pkg: "@fontsource/open-sans", css: "latin-ext-400.css", family: "open-sans" },
  { pkg: "@fontsource/open-sans", css: "latin-ext-500.css", family: "open-sans" },
  { pkg: "@fontsource/roboto", css: "latin-500.css", family: "roboto" },
  { pkg: "@fontsource/roboto", css: "latin-700.css", family: "roboto" },
  { pkg: "@fontsource/roboto", css: "latin-ext-500.css", family: "roboto" },
  { pkg: "@fontsource/roboto", css: "latin-ext-700.css", family: "roboto" },
];

function rewriteFontUrls(css, family) {
  return css.replace(/url\(\.\/files\/([^)]+)\)/g, `url(/fonts/${family}/$1)`);
}

function bundleSiteFonts() {
  const parts = [
    "/* Self-hosted via @fontsource — generated by scripts/sync-vendor-assets.mjs */",
  ];

  for (const { pkg, css, family } of FONT_CSS) {
    const pkgDir = join(root, "node_modules", pkg);
    const cssPath = join(pkgDir, css);
    const raw = readFileSync(cssPath, "utf8");
    const outDir = join(root, "public", "fonts", family);
    mkdirSync(outDir, { recursive: true });

    for (const match of raw.matchAll(/url\(\.\/files\/([^)]+)\)/g)) {
      const filename = match[1];
      cpSync(join(pkgDir, "files", filename), join(outDir, filename));
    }

    parts.push(rewriteFontUrls(raw, family));
  }

  const outCss = join(root, "public", "css", "site-fonts.css");
  mkdirSync(dirname(outCss), { recursive: true });
  writeFileSync(outCss, `${parts.join("\n\n")}\n`, "utf8");

  const mirrorCss = join(root, "css", "site-fonts.css");
  mkdirSync(dirname(mirrorCss), { recursive: true });
  cpSync(outCss, mirrorCss);
}

function syncFontAwesome() {
  const src = join(root, "node_modules", "@fortawesome", "fontawesome-free");
  const dest = join(root, "public", "vendor", "font-awesome");
  mkdirSync(join(dest, "css"), { recursive: true });
  cpSync(join(src, "css", "all.min.css"), join(dest, "css", "all.min.css"));
  cpSync(join(src, "webfonts"), join(dest, "webfonts"), { recursive: true });
}

export function syncVendorAssets() {
  bundleSiteFonts();
  syncFontAwesome();
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  syncVendorAssets();
  console.log("Vendor assets synced (site-fonts.css, Font Awesome).");
}
